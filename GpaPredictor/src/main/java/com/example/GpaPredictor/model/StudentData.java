package com.example.GpaPredictor.model;

public class StudentData {
    private Integer age;
    private Integer gender;
    private Integer ethnicity;
    private Integer parentalEducation;
    private Integer studyTimeWeekly;
    private Integer absences;
    private Integer tutoring;
    private Integer parentalSupport;
    private Integer extracurricular;
    private Integer sports;
    private Integer music;
    private Integer volunteering;

    // Getters and Setters

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getGender() {
        return gender;
    }

    public void setGender(Integer gender) {
        this.gender = gender;
    }

    public Integer getEthnicity() {
        return ethnicity;
    }

    public void setEthnicity(Integer ethnicity) {
        this.ethnicity = ethnicity;
    }

    public Integer getParentalEducation() {
        return parentalEducation;
    }

    public void setParentalEducation(Integer parentalEducation) {
        this.parentalEducation = parentalEducation;
    }

    public Integer getStudyTimeWeekly() {
        return studyTimeWeekly;
    }

    public void setStudyTimeWeekly(Integer studyTimeWeekly) {
        this.studyTimeWeekly = studyTimeWeekly;
    }

    public Integer getAbsences() {
        return absences;
    }

    public void setAbsences(Integer absences) {
        this.absences = absences;
    }

    public Integer getTutoring() {
        return tutoring;
    }

    public void setTutoring(Integer tutoring) {
        this.tutoring = tutoring;
    }

    public Integer getParentalSupport() {
        return parentalSupport;
    }

    public void setParentalSupport(Integer parentalSupport) {
        this.parentalSupport = parentalSupport;
    }

    public Integer getExtracurricular() {
        return extracurricular;
    }

    public void setExtracurricular(Integer extracurricular) {
        this.extracurricular = extracurricular;
    }

    public Integer getSports() {
        return sports;
    }

    public void setSports(Integer sports) {
        this.sports = sports;
    }

    public Integer getMusic() {
        return music;
    }

    public void setMusic(Integer music) {
        this.music = music;
    }

    public Integer getVolunteering() {
        return volunteering;
    }

    public void setVolunteering(Integer volunteering) {
        this.volunteering = volunteering;
    }
}

